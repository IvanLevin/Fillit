int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb > 0 && nb < 2147395600)
		while (i * i <= nb)
		{
			if (nb == i * i)
				return (i);
			else
				i++;
		}
	else if (i * i > nb)
		return (i);
	return (i);
}
